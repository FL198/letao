$.ajax({
    url:'/cart/queryCart',
    success:function(info){
      if(info.error===400){
        window.location.href="../html/login.html?url=../html/user.html"
      }
    }
})
$('.exit').click(function(){
    $.ajax({
      url:'/user/logout',
      dataType:'json',
      success:function(info){
        if(info.success){
          window.location.href="../html/login.html?url=../html/user.html"
        }
      }
    })
})
